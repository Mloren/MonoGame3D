using System;
using System.Collections.Generic;
using System.Text;
using Microsoft.Xna.Framework.Input;

//----------------------------------------------------------------------------------
//----------------------------------------------------------------------------------
namespace GameEngine
{
	//----------------------------------------------------------------------------------
	//----------------------------------------------------------------------------------
	public class Input
	{
		private KeyboardState m_PrevState;
		private KeyboardState m_CurrentState;

		//----------------------------------------------------------------------------------
		//----------------------------------------------------------------------------------
		public void EarlyUpdate()
		{
			m_CurrentState = Keyboard.GetState();
		}

		//----------------------------------------------------------------------------------
		//----------------------------------------------------------------------------------
		public void LateUpdate()
		{
			m_PrevState = m_CurrentState;
		}

		//----------------------------------------------------------------------------------
		//----------------------------------------------------------------------------------
		public bool GetKeyDown(Keys key)
		{
			return (!m_PrevState.IsKeyDown(key) && m_CurrentState.IsKeyDown(key));
		}

		//----------------------------------------------------------------------------------
		//----------------------------------------------------------------------------------
		public bool GetKeyUp(Keys key)
		{
			return (!m_PrevState.IsKeyUp(key) && m_CurrentState.IsKeyUp(key));
		}

		//----------------------------------------------------------------------------------
		//----------------------------------------------------------------------------------
		public bool GetKey(Keys key)
		{
			return m_CurrentState.IsKeyDown(key);
		}
	}
}
