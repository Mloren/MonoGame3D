using System;
using System.Collections.Generic;
using System.Text;

//----------------------------------------------------------------------------------
//----------------------------------------------------------------------------------
namespace GameEngine
{
	//----------------------------------------------------------------------------------
	//----------------------------------------------------------------------------------
	public class Component
	{
		private GameObject m_oParentObject = null;

		//----------------------------------------------------------------------------------
		//----------------------------------------------------------------------------------
		public void SetGameObject(GameObject oGameObject)
		{
			m_oParentObject = oGameObject;
		}

		//----------------------------------------------------------------------------------
		//----------------------------------------------------------------------------------
		public GameObject GetGameObject()
		{
			return m_oParentObject;
		}
	}
}
